#include <ardev/events/EventType.h>





static int _eventIdCounter = 0;





/**************************************************************************
 *
 * 
 *
 **************************************************************************/

EventType *EventType_init(EventType *self) {

    int id = ++_eventIdCounter;

    self->id = id;

    return self;
}





/**************************************************************************
 *
 * 
 *
 **************************************************************************/

int EventType_getId(EventType *self) {

    return self->id;
}





